/*******************************************************************************
 * FunctionName : Power
 * Description  : 计算 x 的 y 次方
 * Param        : u8 x 
 * Param        : u8 y 
 * Return       : u32   
*******************************************************************************/
u32 Power(u8 x, u8 y)
{
    u32 m = x;

    if (y == 0) return 1;
    while (--y) m *= x;
    return m;
}

/*******************************************************************************
 * FunctionName : Exp
 * Description  : 计算 10 的 x 次方
 * Param        : u8 x 
 * Return       : u32   
*******************************************************************************/
u32 Exp(u8 x)
{
    u32 m = 1;

    while (x--) m *= 10;
    return m;
}

/*******************************************************************************
 * FunctionName : SeekStr
 * Description  : 在数据区中查找第 Idx 个字符串的起始地址
 * Param        : u8* ptr 
 * Param        : u8 Idx 
 * Return       : u8*   
*******************************************************************************/
u8* SeekStr(u8 *ptr, u8 Idx)
{
    while (Idx--) while (*ptr++);
    return ptr;
}

/*******************************************************************************
 * FunctionName : Value2Str
 * Description  : 32位数转e位有效数字符串 + 量纲字符串（结构为Unit[][6]）+  模式
 * Param        : u8* p 
 * Param        : s32 n 
 * Param        : uc8* pUnit 
 * Param        : s8 e 
 * Param        : u8 Mode 
*******************************************************************************/
void Value2Str(u8 *p, s32 n, uc8 *pUnit, s8 e, u8 Mode)
{
    s16 i = 0;
    s32 m = n, c = 5;

    if (Mode == SIGN)
    {
//      if (n == 0) *p++ = ' ';
//      if (n >  0) *p++ = '+';
        if (n <  0)
        {*p++ = '-'; n = -n;}
    } 
//  else *p++ = ' ';
 
    while (m >= 10)               // 计算 n 的有效位数 i
    {m /= 10; i++;} 
    if ((i % 3 == 2) && (e == 2)) e++;
    m = n; i = 0;
    while (m >= 10)
    {
        m /= 10;
        if (++i > e) c *= 10;     // n 的有效位数 i 大于e则计算四舍五入值
    }
    if (i >= e) n += c;           // n 加上四舍五入值
    m = n; i = 0;
    while (m >= 10)               // 重新计算 n 的有效位数 i
    {m /= 10; i++;}

    m = i / 3;                    // 计算量纲单位取值偏移量
    while (e--)
    {
        *p++ = '0' + n / Exp(i);
        if (e && (i % 3 == 0)) *p++ = '.';
        n = (i < 0) ? 0 : n % Exp(i);
        i--;
    }
    pUnit += 6 * m;                
    do
    {
        *p++ = *pUnit;
    }
    while (*pUnit++);             // 加上量纲字符字符串
}


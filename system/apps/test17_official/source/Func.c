/*******************************************************************************
 * FunctionName : u16ToDec4Str
 * Description  : 无符号16位二进制数转4位十进制字符串，有效数字前填空格
 * Param        : u8* p 
 * Param        : u16 n 
*******************************************************************************/
void u16ToDec4Str(u8 *p, u16 n)
{
    if (n / 10000)
    {
        *p++ = 'O';
        *p++ = 'v';
        *p++ = 'e';
        *p++ = 'r';
        *p   = 0;
        return;
    }
    *p++ = '0' + n / 1000;
    n %= 1000;
    *p++ = '0' + n / 100;
    n %= 100;
    *p++ = '0' + n / 10;
    n %= 10;
    *p++ = '0' + n;
    *p = 0;
    if (p[-4] == '0')
    {
        p[-4] = ' ';
        if (p[-3] == '0')
        {
            p[-3] = ' ';
            if (p[-2] == '0') p[-2] = ' ';
        }
    }
}

